division_enteros = 5//2
print("division entre 5 y 2")
print("division_enteros=",division_enteros)
print("resto=",resto)

resto = 11%3
division_enteros = 11 //3
print("division entre 11 y 3")
print("division_enteros=",division_enteros)
print("resto=",resto)

resto = 12%4
