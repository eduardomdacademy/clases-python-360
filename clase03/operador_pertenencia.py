lista_1 = ["frutilla", "manzana","pera", "sandia"]
resultado = "manzana" in lista_1 #True porque uvas se encuentra en la lista
print('"manzana" in lista_1', resultado)

resultado = "uvas" in lista_1 # False porque uvas no se encuentra en la lista
print('"uvas" in lista_1', resultado)

resultado = "uvas" not in lista_1 #True porque uvas no se encuentra en la lista
print('"uvas" in lista_1', resultado)